    public void Display() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        try {
        
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/airline" , "root" , "");
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("select * from air");
            
            while(rs.next()) {
                Object[] rowData = new Object[8];
                rowData[0] = rs.getInt("Ticket No");
                rowData[1] = rs.getString("Class");
                rowData[2] = rs.getString("Location");
                rowData[3] = rs.getString("Destination");
                rowData[4] = rs.getString("Ticket Type");
                rowData[5] = rs.getInt("Price");
                rowData[6] = rs.getInt("Children");
                rowData[7] = rs.getInt("Adults");
                
                model.addRow(rowData);
                //model.addRow(new Object[](rs.getInt(1) , rs.getString(2) , rs.getString(3) rs.getString(4) , rs.getString(5) , rs.getInt(6) , rs.getInt(7) , rs.getInt(8) ) );
            }
            
            
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

